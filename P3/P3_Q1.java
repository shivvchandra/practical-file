public class P3_Q1 {
    public static void main(String[] args) {
        int i = 1;

        //prints i from 1 to 10
        while (i < 10) {
            System.out.println(i);
            i++;
        }

        //prints i from 10 to 1
        while (i > 0) {
            System.out.println(i);
            i--;
        }
    }
}